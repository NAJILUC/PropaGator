static void mdlStart_c4_OneToOne(SimStruct *S)
{
  SFc4_OneToOneInstanceStruct *chartInstance;
  ChartRunTimeInfo * crtInfo = (ChartRunTimeInfo *)utMalloc(sizeof
    (ChartRunTimeInfo));
  chartInstance = (SFc4_OneToOneInstanceStruct *)utMalloc(sizeof
    (SFc4_OneToOneInstanceStruct));
  memset(chartInstance, 0, sizeof(SFc4_OneToOneInstanceStruct));
  if (chartInstance==NULL) {
    sf_mex_error_message("Could not allocate memory for chart instance.");
  }

  chartInstance->chartInfo.chartInstance = chartInstance;
  chartInstance->chartInfo.isEMLChart = 1;
  chartInstance->chartInfo.chartInitialized = 0;
  chartInstance->chartInfo.sFunctionGateway = sf_opaque_gateway_c4_OneToOne;
  chartInstance->chartInfo.initializeChart = sf_opaque_initialize_c4_OneToOne;
  chartInstance->chartInfo.terminateChart = sf_opaque_terminate_c4_OneToOne;
  chartInstance->chartInfo.enableChart = sf_opaque_enable_c4_OneToOne;
  chartInstance->chartInfo.disableChart = sf_opaque_disable_c4_OneToOne;
  chartInstance->chartInfo.getSimState = sf_opaque_get_sim_state_c4_OneToOne;
  chartInstance->chartInfo.setSimState = sf_opaque_set_sim_state_c4_OneToOne;
  chartInstance->chartInfo.getSimStateInfo = sf_get_sim_state_info_c4_OneToOne;
  chartInstance->chartInfo.zeroCrossings = NULL;
  chartInstance->chartInfo.outputs = NULL;
  chartInstance->chartInfo.derivatives = NULL;
  chartInstance->chartInfo.mdlRTW = mdlRTW_c4_OneToOne;
  chartInstance->chartInfo.mdlStart = mdlStart_c4_OneToOne;
  chartInstance->chartInfo.mdlSetWorkWidths = mdlSetWorkWidths_c4_OneToOne;
  chartInstance->chartInfo.extModeExec = NULL;
  chartInstance->chartInfo.restoreLastMajorStepConfiguration = NULL;
  chartInstance->chartInfo.restoreBeforeLastMajorStepConfiguration = NULL;
  chartInstance->chartInfo.storeCurrentConfiguration = NULL;
  chartInstance->chartInfo.debugInstance = sfGlobalDebugInstanceStruct;
  chartInstance->S = S;
  crtInfo->instanceInfo = (&(chartInstance->chartInfo));
  crtInfo->isJITEnabled = false;
  ssSetUserData(S,(void *)(crtInfo));  /* register the chart instance with simstruct */
  init_dsm_address_info(chartInstance);
  if (!sim_mode_is_rtw_gen(S)) {
  }

  sf_opaque_init_subchart_simstructs(chartInstance->chartInfo.chartInstance);
  chart_debug_initialization(S,1);
}

void c4_OneToOne_method_dispatcher(SimStruct *S, int_T method, void *data)
{
  switch (method) {
   case SS_CALL_MDL_START:
    mdlStart_c4_OneToOne(S);
    break;

   case SS_CALL_MDL_SET_WORK_WIDTHS:
    mdlSetWorkWidths_c4_OneToOne(S);
    break;

   case SS_CALL_MDL_PROCESS_PARAMETERS:
    mdlProcessParameters_c4_OneToOne(S);
    break;

   default:
    /* Unhandled method */
    sf_mex_error_message("Stateflow Internal Error:\n"
                         "Error calling c4_OneToOne_method_dispatcher.\n"
                         "Can't handle method %d.\n", method);
    break;
  }
}
